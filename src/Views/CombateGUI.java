    private void btnAtkPoke1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtkPoke1ActionPerformed
        btnAtkPoke1.setVisible(false);
        btnAtkPoke2.setVisible(false);
        btnVoltar.setVisible(false);
        if(turnoDe){
        //ataque do p1
        p1.atkPrimario.acao(p2);
        lblNarracao.setText(p1.getNome() + " utilizou " + p1.atkPrimario.getNome());
        showInfoPoke();
        this.turnoDe = false;
        btnContinuar.setVisible(true);
        } else {
        //ataque do p2
        p2.atkPrimario.acao(p1);
        lblNarracao.setText(p2.getNome() + " utilizou " + p2.atkPrimario.getNome());
        showInfoPoke();
        this.turnoDe = true;
         btnContinuar.setVisible(true);
        } 
    }//GEN-LAST:event_btnAtkPoke1ActionPerformed

