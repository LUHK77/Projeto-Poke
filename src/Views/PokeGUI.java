    public void showPoke() {
    Poke poke = this.pokes.getFirst();
    imgPoke.setIcon(new javax.swing.ImageIcon(getClass().getResource(poke.getImagePath())));
    lblNome.setText(poke.getNome());
    atkBar.setValue(poke.getAtaqueBase());
    String atk = String.valueOf(poke.getAtaqueBase());
    atkValue.setText(atk);
    hpBar.setValue(poke.getMaxHp());
    String hp = String.valueOf(poke.getMaxHp());
    hpValue.setText(hp);
    stBar.setValue(poke.getMaxStamina());
    String st = String.valueOf(poke.getMaxStamina());
    stValue.setText(st);
    } 
    
    public void showPoke1(Poke poki) {
    Poke poke = poki;
    String id = String.valueOf(poke.getId());
    lblId.setText(id);
    imgPoke.setIcon(new javax.swing.ImageIcon(getClass().getResource(poke.getImagePath())));
    lblNome.setText(poke.getNome());
    atkBar.setValue(poke.getAtaqueBase());
    String atk = String.valueOf(poke.getAtaqueBase());
    atkValue.setText(atk);
    hpBar.setValue(poke.getMaxHp());
    String hp = String.valueOf(poke.getMaxHp());
    hpValue.setText(hp);
    stBar.setValue(poke.getMaxStamina());
    String st = String.valueOf(poke.getMaxStamina());
    stValue.setText(st);
    } 
    
